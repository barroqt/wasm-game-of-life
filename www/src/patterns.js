function parsePattern(rows) {
  const cells = [];
  for (let r = 0; r < rows.length; r++) {
    for (let c = 0; c < rows[r].length; c++) {
      if (rows[r][c] === "#") cells.push([r, c]);
    }
  }
  return cells;
}

